#Creating a function
def Bro():
    pass

#Defining a function
def Bro():
    print("I'm a non returning function")
    print("Byeeeeeeeeeeeeeeeeeeeeeeeeee")

#Calling the function
Bro()

#Passing in an argument
def Bro(args):
    print(args)
    Bro("Non Returning Passing in an argument")

#Return Function
def Bro(args):
    return args
print(Bro("Passing in an argument"))